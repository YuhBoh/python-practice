course = "Python Programming"

# len = length
print(len(course))
# Output: 18

print(course[0])
# Output: P

print(course[-1])
# Output: g

print(course[0:3])
# Output: Pyt

print(course[0:])
# Output: Python Programming

print(course[:3])
# Output: Pyt
# If no int, then default is 0

print(course[:])
# Output: Python Programming
# Will be copy of string

print(course[2:-1])
# Output: thon Programmin
